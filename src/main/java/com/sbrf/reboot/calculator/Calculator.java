package com.sbrf.reboot.calculator;

public class Calculator {
    public static void main(String[] args) {

    }
    public static double getAddition(double x, double y) {
        return x + y;
    }
    public static double getSubtraction(double x, double y) {
        return x - y;
    }
    public static double getMultiplication(double x, double y) {
        return x * y;
    }
    public static double getDivision(double x, double y) {
        return x / y;
    }
    public static double getSquare(double x) {
        return x * x;
    }
    public static double getReverse(double x) {
       return 1 / x;
    }
    public static double getSqrt(double x) {
        return Math.sqrt(x);
    }
}